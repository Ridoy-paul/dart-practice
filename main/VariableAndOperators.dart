void main() {
// Object type variable:
  Object name = "Ridoy Paul";
  print(name);

  // Nullable type variable:
  String? varName;
  print(varName);

  // Variable Define
  var firstName = "Ridoy ";
  var lastName = "Paul";
  var age = 24;

  //Type safety Variable define ---
  String myName = "Ridoy Chandra Paul";
  int myAge = 24;

  //Same type variable concate
  print(firstName + lastName);

  // Different typs variable concate.
  print("Name: $myName \nAge: $age");

  //Final keywords is define for static variable which is unchanged
  final staticVariable = 10000;
  print(staticVariable);

  int a = 1;
  int b = 3;
  int c = 100;
  print(a + b + c);
}
